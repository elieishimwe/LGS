													<p>This workshop builds on and refines an earlier work by the author, which presented a set of fundamentals in the measurement of performance in capacity building. It adds value to existing knowledge in two areas. First, it defines four processes, which capacity building (it argues) involves, and second it specifies areas in which performance measurement can and, indeed (it contends), should be undertaken. The presentation is consistent with the input-process-output-outcome-impact framework, which underlies plausible performance measures. This framework represents an expansion of the objective, purpose and impact components of a capacity-building intervention, which are central in the measurement of performance. In other words, performance measures that do not capture in a systematic manner the objective, purpose and impact of a given intervention cannot be regarded as complete and plausible. The Note posits that capacity building involves four processes â€“ development, retooling, enhancement, and reform â€“ and performance measurement should be based on a set of five indicators â€“ finance; quantity; relevance, effectiveness, efficiency, ownership and sustainability; results/outcomes; and impact of an intervention.</p>
